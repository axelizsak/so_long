#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*p1;
	char	*p2;
	int		i;

	p1 = (char *)s;
	p2 = 0;
	i = 0;
	while (p1[i])
	{
		if (p1[i] == (char)c)
			p2 = p1 + i;
		i++;
	}
	if (c == '\0')
		return (p1 + i);
	return (p2);
}
