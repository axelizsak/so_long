#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	n;
	int	i;

	n = 0;
	i = 1;
	if (!*nptr)
		return (0);
	while (*nptr == ' ' || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			i *= -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		n *= 10;
		n += *nptr - '0';
		nptr++;
	}
	return (n * i);
}
